package java02;
public class loops {
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            System.out.print(i+" ");

        }

        int i = 1;
        while (i <= 5) {
            System.out.println("Count: " + i);
            i++; }

        int k = 10; 
        do { 
            System.out.println("Count: " + k); 
            k++;
         } while (k <= 20);
    }
    
}
